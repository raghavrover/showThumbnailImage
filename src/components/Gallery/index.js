class Gallery extends Component {
  state = {
    imageUrl: imagesList[0].imageUrl,
    altText: imagesList[0].imageAltText,
    id: imagesList[0].id,
  }

  getBgImage = eachObj => {
    this.setState({imageUrl: eachObj.imageUrl})
    this.setState({altText: eachObj.imageAltText})
    this.setState({id: eachObj.id})
  }

  render() {
    const {imageUrl, altText, id} = this.state

    return (
      <div className="gallery-container">
        <div className="gallery-card">
          <img src={imageUrl} className="card-image" alt={altText} />
          <h1 className="title">Nature Photography</h1>
          <p className="photography-by">
            Nature Photography by
            <span className="photographer-name">Raghav</span>
          </p>
          <ul className="thumbnails-list">
            {imagesList.map(imageDetails => (
              <ThumbnailItem
                selectedId={id}
                key={imageDetails.id}
                eachObj={imageDetails}
                getBgImage={this.getBgImage}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
